function products(name,alias,price,description,size,shortDescription,quantity,deleted,categories,feature,image){
    this.name = name;
    this.alias = alias;
    this.price = price;
    this.description = description;
    this.size = size;
    this.shortDescription = shortDescription;
    this.quantity = quantity;
    this.deleted = deleted;
    this.categories = categories;
    this.feature = feature;
    this.image = image;
}
